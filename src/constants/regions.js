export function links(){
    let links = [];
    regions.forEach((r,idx,arr)=>{
        r.landAdjacent.forEach((id,idx,arr)=>{
            links.push({
                source:r.id,
                target:id,
                lineStyle:landAdjacentLineStyle
            })
        })
        r.mountainAdjacent.forEach((id,idx,arr)=>{
            links.push({
                source:r.id,
                target:id,
                lineStyle:mountainAdjacentLineStyle
            })
        })
        r.adjacentThroughMountainPass.forEach((id,idx,arr)=>{
            links.push({
                source:r.id,
                target:id,
                lineStyle:mountainPassAdjacentLineStyle
            })
        })
        r.waterAdjacent.forEach((id,idx,arr)=>{
            links.push({
                source:r.id,
                target:id,
                lineStyle:waterAdjacentLineStyle
            })
        })
    })
    return links;
}
